# LEC 4: Data Types, Variables & Operators
# Integer operations
a = 10
b = 3
print("Integers:")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)

# Float operations
x = 7.5
y = 2.0
print("\nFloats:")
print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x / y)
print("x % y =", x % y)

# String concatenation and repetition
s1 = "Hello"
s2 = "World"
print("\nStrings:")
print("Concatenation:", s1 + " " + s2)
print("Repetition:", (s1 + " ") * 2)
