# LEC 8: Python Operators demonstration

# Arithmetic operators: 
a = 7
b = 3
print("Arithmetic operators:")
print("a + b =", a + b)   # addition
print("a - b =", a - b)   # subtraction
print("a * b =", a * b)   # multiplication
print("a / b =", a / b)   # true division
print("a % b =", a % b)   # modulus (remainder)
print("a // b =", a // b) # floor division
print("a ** b =", a ** b) # exponentiation

# Comparison operators:
x = 5
y = 5
print("\nComparison operators:")
print("x == y ->", x == y) # equal
print("x != y ->", x != y) # not equal
print("x < y ->", x < y)   # less than
print("x > y ->", x > y)   # greater than
print("x <= y ->", x <= y) # less than or equal
print("x >= y ->", x >= y) # greater than or equal

# Logical operators: 
p = True
q = False
print("\nLogical operators:")
print("p and q ->", p and q) # True if both p and q are True
print("p or q ->", p or q)   # True if at least one is True
print("not p ->", not p)     # True if p is False
