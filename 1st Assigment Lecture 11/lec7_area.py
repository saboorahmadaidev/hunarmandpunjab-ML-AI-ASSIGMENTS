# LEC 7: Function to calculate area of rectangle

def calculate_area(length, width):
    return length * width

# Call the function with different values
print("Area 1 (2 x 3):", calculate_area(2, 3))
print("Area 2 (5.5 x 4):", calculate_area(5.5, 4))
print("Area 3 (10 x 0.75):", calculate_area(10, 0.75))
